package javastudy03;

public class KBCustomer {
	private int age;
	private String name;
	private String cNum;
	public int getAge() {
		return age;
	}
	
	public void setAge(int age) {
		this.age = age;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getcNum() {
		return cNum;
	}
	
	public void setcNum(String cNum) {
		this.cNum = cNum;
	}

}
